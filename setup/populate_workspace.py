def _populate_calendar(cal_svc, conn, tool_ids, created, skipped):
    cal_event_ids = tool_ids.setdefault("google", {}).setdefault("calendar_events", {})

    for ev in _EVENTS:
        key = ev["key"]
        title = ev["title"]

        if key in cal_event_ids:
            skipped.append(f"Event '{title}'")
            continue

        print(f"  [cal] Creating: {title} ...", end=" ", flush=True)
        result = cal_svc.events().insert(
            calendarId=_CALENDAR_ID,
            body={
                "summary": title,
                "description": ev["description"],
                "start": {"dateTime": ev["start"], "timeZone": _TIMEZONE},
                "end":   {"dateTime": ev["end"],   "timeZone": _TIMEZONE},
                "attendees": [{"email": e} for e in ev.get("attendees", [])],
            },
        ).execute()
        google_event_id = result["id"]
        print(f"done ({google_event_id})")
        cal_event_ids[key] = google_event_id

        # SQLite: calendar_events row
        canonical_id = _next_cal_id(conn)
        conn.execute(
            """
            INSERT INTO calendar_events
                (id, title, event_type, start_datetime, end_datetime, attendees, notes)
            VALUES (?, ?, ?, ?, ?, ?, ?)
            """,
            (
                canonical_id, title, ev["event_type"],
                ev["start"], ev["end"],
                json.dumps(ev.get("attendees", [])),
                ev["description"],
            ),
        )
        conn.commit()
        created.append(f"Event '{title}' → {google_event_id}")


