def _create_google_doc(docs_svc, title: str, content: str) -> str:
    """Create a Google Doc, insert content at index 1, return document ID."""
    doc = docs_svc.documents().create(body={"title": title}).execute()
    doc_id = doc["documentId"]
    docs_svc.documents().batchUpdate(
        documentId=doc_id,
        body={
            "requests": [
                {
                    "insertText": {
                        "location": {"index": 1},
                        "text": content,
                    }
                }
            ]
        },
    ).execute()
    return doc_id


def _populate_docs(docs_svc, conn, tool_ids, created, skipped):
    doc_ids = tool_ids.setdefault("google", {}).setdefault("docs", {})

    for defn in _DOCS:
        key = defn["key"]
        title = defn["title"]

        if key in doc_ids:
            skipped.append(f"Doc '{title}'")
            continue

        print(f"  [doc] Generating: {title} ...", end=" ", flush=True)
        content = _generate(defn["prompt"], defn["max_tokens"])
        print("generated.", end=" ", flush=True)

        google_id = _create_google_doc(docs_svc, title, content)
        print(f"created ({google_id})")
        doc_ids[key] = google_id

        # SQLite: documents row
        canonical_id = _next_doc_id(conn)
        keywords = _extract_keywords(content)
        conn.execute(
            """
            INSERT INTO documents
                (id, title, doc_type, platform, google_file_id,
                 content_text, keywords, last_indexed_at)
            VALUES (?, ?, ?, 'google_docs', ?, ?, ?, datetime('now'))
            """,
            (canonical_id, title, defn["doc_type"], google_id,
             content[:5000], keywords),
        )

        # SQLite: document_index chunks (1 000-char chunks)
        for i in range(0, len(content), 1000):
            chunk = content[i : i + 1000]
            conn.execute(
                """
                INSERT INTO document_index (doc_id, chunk_text, keywords, source_title)
                VALUES (?, ?, ?, ?)
                """,
                (canonical_id, chunk, _extract_keywords(chunk, 8), title),
            )

        conn.commit()
        created.append(f"Doc '{title}' → {google_id}")


