# Google Sheets
# ------------------------------------------------------------------ #
_HEADER_BG = {"red": 0.18, "green": 0.48, "blue": 0.58}
_HEADER_FG = {"red": 1.0,  "green": 1.0,  "blue": 1.0}
_ALT_ROW_BG = {"red": 0.93, "green": 0.96, "blue": 0.98}


def _create_google_sheet(sheets_svc, title: str, tab_name: str, headers: list, rows: list) -> str:
    """Create a Sheet, write data, apply header + alternating-row formatting."""
    spreadsheet = sheets_svc.spreadsheets().create(
        body={
            "properties": {"title": title},
            "sheets": [{"properties": {"title": tab_name}}],
        }
    ).execute()
    ss_id = spreadsheet["spreadsheetId"]
    sheet_id = spreadsheet["sheets"][0]["properties"]["sheetId"]

    # Write headers + data
    all_rows = [headers] + [[str(v) for v in row] for row in rows]
    sheets_svc.spreadsheets().values().update(
        spreadsheetId=ss_id,
        range=f"{tab_name}!A1",
        valueInputOption="USER_ENTERED",
        body={"values": all_rows},
    ).execute()

    num_cols = len(headers)
    fmt_requests = []

    # Bold + coloured header row
    fmt_requests.append({
        "repeatCell": {
            "range": {
                "sheetId": sheet_id,
                "startRowIndex": 0, "endRowIndex": 1,
                "startColumnIndex": 0, "endColumnIndex": num_cols,
            },
            "cell": {
                "userEnteredFormat": {
                    "backgroundColor": _HEADER_BG,
                    "textFormat": {"foregroundColor": _HEADER_FG, "bold": True},
                }
            },
            "fields": "userEnteredFormat(backgroundColor,textFormat)",
        }
    })

    # Alternating rows (every odd data row)
    for idx in range(len(rows)):
        if idx % 2 == 1:
            fmt_requests.append({
                "repeatCell": {
                    "range": {
                        "sheetId": sheet_id,
                        "startRowIndex": idx + 1, "endRowIndex": idx + 2,
                        "startColumnIndex": 0, "endColumnIndex": num_cols,
                    },
                    "cell": {
                        "userEnteredFormat": {"backgroundColor": _ALT_ROW_BG}
                    },
                    "fields": "userEnteredFormat(backgroundColor)",
                }
            })

    # Auto-resize columns
    fmt_requests.append({
        "autoResizeDimensions": {
            "dimensions": {
                "sheetId": sheet_id,
                "dimension": "COLUMNS",
                "startIndex": 0,
                "endIndex": num_cols,
            }
        }
    })

    sheets_svc.spreadsheets().batchUpdate(
        spreadsheetId=ss_id,
        body={"requests": fmt_requests},
    ).execute()

    return ss_id


def _populate_sheets(sheets_svc, conn, tool_ids, created, skipped):
    sheet_ids = tool_ids.setdefault("google", {}).setdefault("sheets", {})

    for defn in _SHEETS:
        key = defn["key"]
        title = defn["title"]

        if key in sheet_ids:
            skipped.append(f"Sheet '{title}'")
            continue

        print(f"  [sheet] Creating: {title} ...", end=" ", flush=True)
        ss_id = _create_google_sheet(
            sheets_svc, title, defn["tab_name"],
            defn["headers"], defn["rows"],
        )
        print(f"done ({ss_id})")
        sheet_ids[key] = ss_id

        # SQLite: documents row
        canonical_id = _next_doc_id(conn)
        kw_text = title + " " + " ".join(defn["headers"])
        conn.execute(
            """
            INSERT INTO documents
                (id, title, doc_type, platform, google_file_id,
                 content_text, keywords, last_indexed_at)
            VALUES (?, ?, 'spreadsheet', 'google_sheets', ?, ?, ?, datetime('now'))
            """,
            (canonical_id, title, ss_id,
             ", ".join(defn["headers"]), _extract_keywords(kw_text)),
        )
        conn.commit()
        created.append(f"Sheet '{title}' → {ss_id}")


