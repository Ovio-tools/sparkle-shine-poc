# ------------------------------------------------------------------ #
# Anthropic client (lazy)
# ------------------------------------------------------------------ #
_MODEL = "claude-sonnet-4-0"
_anthropic_client: Optional[anthropic.Anthropic] = None


def _get_client() -> anthropic.Anthropic:
    global _anthropic_client
    if _anthropic_client is None:
        _anthropic_client = anthropic.Anthropic(
            api_key=get_credential("ANTHROPIC_API_KEY")
        )
    return _anthropic_client


def _generate(prompt: str, max_tokens: int = 1200) -> str:
    """Return text generated by Claude (streaming)."""
    with _get_client().messages.stream(
        model=_MODEL,
        max_tokens=max_tokens,
        messages=[{"role": "user", "content": prompt}],
    ) as stream:
        final = stream.get_final_message()
    return next(b.text for b in final.content if b.type == "text")


