def _load_tool_ids() -> dict:
    with open(_TOOL_IDS_PATH) as f:
        return json.load(f)


def _save_tool_ids(data: dict) -> None:
    with open(_TOOL_IDS_PATH, "w") as f:
        json.dump(data, f, indent=2)


def _extract_keywords(text: str, max_kw: int = 15) -> str:
    """Return comma-separated top keywords extracted from text."""
    stop = {
        "the", "a", "an", "and", "or", "but", "in", "on", "at", "to", "for",
        "of", "with", "is", "are", "was", "were", "be", "been", "have", "has",
        "had", "do", "does", "did", "will", "would", "could", "should", "may",
        "this", "that", "these", "those", "it", "its", "we", "our", "your",
        "their", "as", "by", "from", "into", "all", "each", "any", "also",
        "not", "no", "only", "so", "than", "too", "very", "just", "more",
    }
    words = re.findall(r"[a-zA-Z]{4,}", text.lower())
    freq: Dict[str, int] = {}
    for w in words:
        if w not in stop:
            freq[w] = freq.get(w, 0) + 1
    top = sorted(freq, key=lambda k: freq[k], reverse=True)[:max_kw]
    return ", ".join(top)


def _next_doc_id(conn: sqlite3.Connection) -> str:
    row = conn.execute(
        "SELECT id FROM documents ORDER BY id DESC LIMIT 1"
    ).fetchone()
    n = int(row["id"].split("-")[-1]) + 1 if row else 1
    return f"SS-DOC-{n:04d}"


def _next_cal_id(conn: sqlite3.Connection) -> str:
    row = conn.execute(
        "SELECT id FROM calendar_events ORDER BY id DESC LIMIT 1"
    ).fetchone()
    n = int(row["id"].split("-")[-1]) + 1 if row else 1
    return f"SS-CAL-{n:04d}"


